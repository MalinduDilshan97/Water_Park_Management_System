    private void progress() {
        tmrSplash = new Timer(50, new ActionListener() {

            @Override
            public void actionPerformed(ActionEvent e) {
                
                pgbLoading.setValue(pgbLoading.getValue() + 1);
                progress=pgbLoading.getValue();
                
                if(progress == 10){
                    lblSet.setText("Initializing System....");
                }else if(progress == 20){
                    lblSet.setText("Connecting To Servers....");
                }else if(progress == 30){
                    lblSet.setText("Connecting To Database....");
                }else if(progress == 40){
                    lblSet.setText("Starting Application....");
                }
                
                if (pgbLoading.getValue() >= pgbLoading.getMaximum()){
                    tmrSplash.stop();
                    Login frmLogin = new Login();
                    Splash_Screen.this.dispose();
                    frmLogin.setVisible(true);
                }
            }
        });
         tmrSplash.start();
    }
