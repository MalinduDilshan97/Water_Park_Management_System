    private void lblAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblAddMouseClicked
        FoodDTO foodDTO = new FoodDTO();
        foodDTO.setFid(txtId.getText());
        foodDTO.setDescription(txtDesc.getText());
        foodDTO.setPrice(Double.parseDouble(txtPrice.getText()));
        
            
            boolean result=false;
        
            try {
                result=ManageFoodController.updateFood(foodDTO);
                if(result){
                    JOptionPane.showMessageDialog(this, "Item has been Update successfully");
                }else{
                    JOptionPane.showMessageDialog(this, "Failed to Update the Item");
                }
                ManageFoodController.releaseFood(reservedFid);
                reservedFid = "";
                txtId.requestFocusInWindow();
                update = false;
                txtId.setText("");
                txtId.setBorder(defaultBorder);
                txtDesc.setText("");
                txtDesc.setBorder(defaultBorder);
                txtPrice.setText("");
                txtPrice.setBorder(defaultBorder);
                lblAdd.setVisible(false);
                valid=0;
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Failed to Update the Item", "Error", JOptionPane.ERROR_MESSAGE);
                Logger.getLogger(Food_Update.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        
    }//GEN-LAST:event_lblAddMouseClicked

