    private void txtDescKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDescKeyTyped
        if(txtDesc.getText().matches("[A-Za-z ]+")){
            txtDesc.setBorder(defaultBorder);
        }else{
            txtDesc.setBorder(BorderFactory.createLineBorder(Color.red));
        }
    }//GEN-LAST:event_txtDescKeyTyped

    private void txtIdKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIdKeyPressed
        if(txtId.getText().matches("f[0-9]+")){
            txtId.setBorder(defaultBorder);
            valid=0;
            check();
        }else{
            txtId.setBorder(BorderFactory.createLineBorder(Color.red));
            valid=1;
            check();
        }
    }//GEN-LAST:event_txtIdKeyPressed

    private void txtIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtIdActionPerformed
        lblAdd.setVisible(true);
        FoodDTO foodDTO = new FoodDTO();
        
         try {
             foodDTO=ManageFoodController.searchFood(txtId.getText());
             
             if(foodDTO!= null){
                 
             }else{
                 JOptionPane.showMessageDialog(this,"Check The  ID Again");
             }
             
         } catch (Exception ex) {
             JOptionPane.showMessageDialog(this, "No data found...", "Error", JOptionPane.ERROR_MESSAGE); 
             txtId.setText("");
             txtId.setBorder(defaultBorder);
            txtDesc.setText("");
            txtDesc.setBorder(defaultBorder);
            txtPrice.setText("");
            txtPrice.setBorder(defaultBorder);
            lblAdd.setVisible(false);
            valid=0;
             
             Logger.getLogger(Food_Update.class.getName()).log(Level.SEVERE, null, ex);
             
         }
        
        try {
                    if (reservedFid == "") {
                        ManageFoodController.releaseFood(reservedFid);
                    }
                    reservedFid = txtId.getText();
                    boolean reserveCategory = ManageFoodController.reserveFood(reservedFid);

                    if (reserveCategory) {
                        update = true;
                        txtDesc.setText(foodDTO.getDescription());
                        txtPrice.setText(""+foodDTO.getPrice());
                    } else {
                        update = false;
                        JOptionPane.showMessageDialog(Food_Update.this, "Item has been already reserved");
                    }

                } catch (Exception ex) {
                    Logger.getLogger(Food_Update.class.getName()).log(Level.SEVERE, null, ex);
                }
    }//GEN-LAST:event_txtIdActionPerformed

    private void txtPriceKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPriceKeyPressed
        if(txtPrice.getText().matches("[0-9]+.[0-9]+")){
            txtPrice.setBorder(defaultBorder);
            valid=0;
            check();
        }else{
            txtPrice.setBorder(BorderFactory.createLineBorder(Color.red));
            valid=1;
            check();
        }
    }//GEN-LAST:event_txtPriceKeyPressed


