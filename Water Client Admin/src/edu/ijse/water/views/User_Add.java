    private void check() {
            if(nameBorder>0){
                if(passBorder>0){
                        lblAdd.setVisible(true);
                }else{
                    lblAdd.setVisible(false);
                }
            }else{
                lblAdd.setVisible(false);
            }
    }

    private void loadID() {
        IDDTO idDTO=new IDDTO("User", "uid");
        try {
            String id=ManageIDController.getNewID(idDTO, "u");
            txtId.setText(id);
        } catch (Exception ex) {
            Logger.getLogger(Food_Add.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void Clear() {
         txtId.setText("");
        txtName.setText("");
        txtPassword.setText("");
        passBorder = 0;
        nameBorder=0;
        txtId.setBorder(defaultBorder);
        txtName.setBorder(defaultBorder);
        txtPassword.setBorder(defaultBorder);
        loadID();
    }
