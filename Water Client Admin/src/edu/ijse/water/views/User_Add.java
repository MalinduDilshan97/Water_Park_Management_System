    private void lblAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblAddMouseClicked
        UserDTO userDTO =new UserDTO();
        userDTO.setUid(txtId.getText());
        userDTO.setUserName(txtName.getText());
        userDTO.setPassword(txtPassword.getText());
        
        boolean result=false;
        
        try {
            result=ManageUserController.addUser(userDTO);
            if(result){
                JOptionPane.showMessageDialog(this, "User has been saved successfully");
                Clear();
            }else{
                JOptionPane.showMessageDialog(this, "Failed to save the User");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Failed to save the User", "Error", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(User_Add.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_lblAddMouseClicked

