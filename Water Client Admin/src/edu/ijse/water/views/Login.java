    private void Set() {
        lblPleasewait.setText("Username And Password dose not Mathch");
        lblPleasewait.setForeground(Color.red);
        lblPleasewait.setBackground(Color.white);
    }

    private void dateAndTime() {
        tmrTime=new Timer(0, new ActionListener() {

           
            public void actionPerformed(ActionEvent e) {
                Date dt = new Date();
                SimpleDateFormat sdf = new SimpleDateFormat("hh: mm: ss");
                lblTime.setText(sdf.format(dt));
            }

        } );
         tmrTime.start();
    }
