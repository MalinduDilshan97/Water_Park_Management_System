    private void lbPowerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbPowerMouseClicked
        if(count==0){
            lblPowerPanel.setVisible(true);
            lblShutdown.setVisible(true);
            lblRestart.setVisible(true);
            count=1;
        }else{
            lblPowerPanel.setVisible(false);
            lblShutdown.setVisible(false);
            lblRestart.setVisible(false);
            count=0; 
        }
        
    }//GEN-LAST:event_lbPowerMouseClicked

