    private void lblAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblAddMouseClicked
        GiftDTO giftDTO = new GiftDTO();
        giftDTO.setGid(txtId.getText());
        giftDTO.setDescription(txtDesc.getText());
        giftDTO.setPrice(Double.parseDouble(txtPrice.getText()));
        
            
            boolean result;
        
            try {
                result=ManageGiftController.addGift(giftDTO);
                if(result){
                    JOptionPane.showMessageDialog(this, "Item has been saved successfully");
                    clear();
                }else{
                    JOptionPane.showMessageDialog(this, "Failed to save the Item");
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Failed to save the Item", "Error", JOptionPane.ERROR_MESSAGE);
                Logger.getLogger(Gift_Add.class.getName()).log(Level.SEVERE, null, ex);
            }
        
    }//GEN-LAST:event_lblAddMouseClicked

