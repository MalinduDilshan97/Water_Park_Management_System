    private void check() {
            if(descBorder>0){
                if(descBorder>0){
                    lblAdd.setVisible(true);
                }else{
                    lblAdd.setVisible(false);
                }
            }else{
                lblAdd.setVisible(false);
            }
    }

    private void loadID() {
        IDDTO idDTO=new IDDTO("Gift", "gid");
        try {
            String id=ManageIDController.getNewID(idDTO, "g");
            txtId.setText(id);
        } catch (Exception ex) {
            Logger.getLogger(Food_Add.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void clear() {
        txtId.setText("");
        txtId.setBorder(defaultBorder);
        txtDesc.setText("");
        txtDesc.setBorder(defaultBorder);
        txtPrice.setText("");
        txtPrice.setBorder(defaultBorder);
        lblAdd.setVisible(false);
        priceBorder = 0;
        descBorder = 0;
        loadID();
    }
