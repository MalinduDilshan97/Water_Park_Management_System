    private void lblAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblAddMouseClicked
        UserDTO userDTO =new UserDTO();
        userDTO.setUid(txtId.getText());
        userDTO.setUserName(txtName.getText());
        userDTO.setPassword(txtPassword.getText());
        
        boolean result=false;
        
        try {
            result=ManageUserController.updateUser(userDTO);
            if(result){
                JOptionPane.showMessageDialog(this, "User has been Update successfully");
                
            }else{
                JOptionPane.showMessageDialog(this, "Failed to Update the User");
            }
                ManageUserController.releaseUser(reservedUid);
                reservedUid = "";
                txtId.setText("");
                txtName.setText("");
                txtPassword.setText("");
                txtId.requestFocusInWindow();
                update = false;
                valid=0; 
                txtId.setBorder(defaultBorder);
                txtName.setBorder(defaultBorder);
                txtPassword.setBorder(defaultBorder);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Failed to Update the User", "Error", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(User_Update.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_lblAddMouseClicked

