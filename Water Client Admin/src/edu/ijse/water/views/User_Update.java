    private void txtIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtIdActionPerformed
        lblAdd.setVisible(true); 
        UserDTO userDTO = new UserDTO();
        
         try {
             userDTO=ManageUserController.searchUser(txtId.getText());
             
             if(userDTO!= null){
                 
             }else{
                 JOptionPane.showMessageDialog(this,"Check The  ID Again");
             }
             
         } catch (Exception ex) {
             JOptionPane.showMessageDialog(this, "No data found...", "Error", JOptionPane.ERROR_MESSAGE); 
             Logger.getLogger(User_Update.class.getName()).log(Level.SEVERE, null, ex);
         }
        
        try {
                    if (reservedUid == "") {
                        ManageUserController.releaseUser(reservedUid);
                    }
                    reservedUid = txtId.getText();
                    boolean reserveCategory = ManageUserController.reserveUser(reservedUid);

                    if (reserveCategory) {
                        update = true;
                        txtName.setText(userDTO.getUserName());
                        txtPassword.setText(userDTO.getPassword());
                    } else {
                        update = false;
                        JOptionPane.showMessageDialog(User_Update.this, "Item has been already reserved");
                    }

                } catch (Exception ex) {
                    Logger.getLogger(User_Update.class.getName()).log(Level.SEVERE, null, ex);
                }
    }//GEN-LAST:event_txtIdActionPerformed


